/**
 * Единый движок формирования названия задачи из текста постановки
 * (ТП-153 → ТП-166 V2). Используется ВСЕМИ точками создания (форма, голос,
 * будущие API/импорт) — второй алгоритм заводить запрещено.
 *
 * V2 (ТП-166): анализируется ВЕСЬ текст, а не первое предложение.
 *  1. markdown-очистка (заголовки, **жирный**, списки, ссылки, код);
 *  2. выбор «ядра» — предложение с максимальным инженерным сигналом
 *     (ведущий глагол действия, маркеры дефекта, позиция в тексте);
 *  3. срез вводных («необходимо…», «хочу…», «цель:», «баг:», …) и
 *     хвостов-пояснений («, потому что…», «, чтобы…», «, например…»);
 *  4. канонизация ведущего глагола: императив/личная форма → инфинитив
 *     («поправь/почини» → «Исправить», «сделай» → «Добавить», …);
 *  5. дефект-фраза без ведущего глагола → «<Модуль>: <суть>» (словарь
 *     модулей продукта) либо «Исправить: <суть>»;
 *  6. «При X ломается Y» → «Y ломается при X» (объект первым);
 *  7. ≤70 символов по границе слова, без финальной точки.
 *
 * Честная граница (сохранена из ТП-153): семантическое переформулирование
 * «своими словами» (номинализация глаголов, подбор синонимов) требует
 * понимания языка — это работа LLM; шов готов: заменяется реализация этой
 * функции, вызовы не меняются. Детерминированные правила не выдумывают
 * контекст — только очищают, выбирают и нормализуют написанное автором.
 */

import { splitSentences, capitalizeFirst } from './sentences'

export const TITLE_MAX_CHARS = 70

// ---------------------------------------------------------------------------
// Markdown → плоский текст (описания приходят и из markdown-редактора)
// ---------------------------------------------------------------------------

function stripMarkdown(text: string): string {
  return (
    text
      // код-блоки целиком — технические детали, не для названия
      .replace(/```[\s\S]*?```/g, ' ')
      .replace(/`([^`]*)`/g, '$1')
      // заголовки/цитаты/маркеры списков в начале строк
      .replace(/^[ \t]*#{1,6}[ \t]+/gm, '')
      .replace(/^[ \t]*>[ \t]?/gm, '')
      .replace(/^[ \t]*(?:[-*+]|\d+[.)])[ \t]+/gm, '')
      // **жирный**, *курсив*, __подчёркнутый__
      .replace(/(\*\*|__)(.*?)\1/g, '$2')
      .replace(/(\*|_)(.*?)\1/g, '$2')
      // [текст](url) → текст
      .replace(/\[([^\]]*)\]\([^)]*\)/g, '$1')
      // Абзац/строка — граница мысли: заголовки и пункты без финальной точки
      // не должны склеиваться со следующим предложением.
      .replace(/[ \t]*\n{2,}[ \t]*/g, '. ')
      .replace(/[ \t]*\n[ \t]*/g, '. ')
      .replace(/\.{2,}/g, '.')
      .replace(/[ \t]+/g, ' ')
  )
}

