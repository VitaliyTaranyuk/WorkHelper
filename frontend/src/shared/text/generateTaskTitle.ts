/** Вводные конструкции, не несущие инженерной сути. Порядок важен: длинные раньше. */
const FILLER_PREFIXES: RegExp[] = [
  /^сделай(?:те)?\s+так\s*,?\s*чтобы\s+/iu,
  /^сделать\s+так\s*,?\s*чтобы\s+/iu,
  /^было\s+бы\s+(?:здорово|хорошо|классно|удобно)\s*,?\s*(?:если\s+бы\s+|чтобы\s+)?/iu,
  /^хотелось\s+бы\s*,?\s*(?:чтобы\s+)?/iu,
  /^хочется\s*,?\s*(?:чтобы\s+)?/iu,
  /^хочу\s*,?\s*(?:чтобы\s+)?/iu,
  /^необходимо\s*,?\s*(?:чтобы\s+)?/iu,
  /^нужно\s*,?\s*(?:чтобы\s+)?/iu,
  /^надо\s*,?\s*(?:чтобы\s+)?/iu,
  /^требуется\s*,?\s*(?:чтобы\s+)?/iu,
  /^следует\s*,?\s*(?:чтобы\s+)?/iu,
  /^важно\s*,?\s*(?:чтобы\s+)?/iu,
  /^предлагаю\s*,?\s*(?:чтобы\s+)?/iu,
  /^предлагается\s*,?\s*/iu,
  /^прошу\s*,?\s*/iu,
  /^пожалуйста\s*,?\s*/iu,
  /^давай(?:те)?\s+/iu,
  /^есть\s+предложение\s*[:,–-]?\s*/iu,
  /^есть\s+(?:баг|проблема|ошибка)\s*[:,–-]?\s*/iu,
  /^(?:цель|задача|суть|проблема|баг|ошибка|итог|кратко)\s*[:.–-]\s*/iu,
  /^реализовать\s+возможность\s+/iu,
  /^добавить\s+возможность\s+/iu,
  /^доработать\s*,?\s*чтобы\s+/iu,
  // «поправь/исправь, что <предикат>» — глагол-вводная, суть в предикате
  // (дальше предикат уйдёт по дефект-пути: «Исправить: …» / «Модуль: …»)
  /^(?:поправь(?:те)?|исправь(?:те)?|почини(?:те)?|пофикси(?:те)?|поправить|исправить|починить|пофиксить)\s*,?\s*(?:то\s*,?\s*)?что\s+/iu,
]

/** Хвосты-пояснения: причина/альтернатива/цель/пример — место им в описании. */
const TAIL_CUTTERS: RegExp[] = [
  /,\s*а\s+не\s+.+$/iu,
  /,\s*потому\s+что\s+.+$/iu,
  /,\s*так\s+как\s+.+$/iu,
  /,\s*поскольку\s+.+$/iu,
  /,\s*чтобы\s+.+$/iu,
  /,\s*например\s*[:,]?\s+.+$/iu,
  /\s+—\s+иначе\s+.+$/iu,
]

function stripFillers(text: string): string {
  let out = text.trim()
  let changed = true
  while (changed) {
    changed = false
    for (const re of FILLER_PREFIXES) {
      const next = out.replace(re, '')
      if (next !== out) {
        out = next.trim()
        changed = true
      }
    }
  }
  return out
}

function cutExplanatoryTail(text: string): string {
  let out = text
  for (const re of TAIL_CUTTERS) {
    // Хвост режем только если остаётся содержательное начало (3+ слова).
    const next = out.replace(re, '')
    if (next !== out && next.trim().split(/\s+/).length >= 3) out = next.trim()
  }
  return out
}

