/** Маркеры дефекта (bug-тип и перестановка «При X … Y»). */
const BREAKAGE_VERBS =
  '(?:ломается|не\\s+работает|не\\s+двигается|не\\s+двигаются|перестал[аио]?\\s+работать|падает|пропадает|пропадают|зависает|съезжает|съезжают|дублируется|дублируются|не\\s+отображается|не\\s+отображаются|не\\s+открывается|не\\s+открываются|не\\s+сохраняется|не\\s+сохраняются|не\\s+обновляется|не\\s+обновляются|не\\s+приходит|не\\s+приходят|тормозит|тупит|глючит|обрывается|теряется|теряются)'

const DEFECT_SIGNAL = new RegExp(
  `${BREAKAGE_VERBS}|ошибк|не\\s+получается|некорректн|неправильн|вместо\\s+`,
  'iu',
)

/** Вопросительное начало: вопрос — не формулировка работы, префиксы не лепим. */
const QUESTION_START =
  /^(?:почему|отчего|зачем|как|что|где|когда|куда|можно\s+ли|стоит\s+ли)(?=[\s,?]|$)/iu

const AT_BREAKAGE = new RegExp(`^при\\s+(.+?)\\s+${BREAKAGE_VERBS}\\s+(.+)$`, 'iu')

/**
 * Канонизация ведущего глагола: императивы и личные формы → инфинитив
 * инженерного действия. Замена только НАЧАЛЬНОГО слова — падежи остального
 * текста не трогаем (честная граница без морфологии).
 */
const VERB_CANON: Array<[RegExp, string]> = [
  [/^(?:поправь(?:те)?|почини(?:те)?|исправь(?:те)?|пофикси(?:те)?|поправить|починить|пофиксить)(?=[\s,]|$)\s*,?\s*(?:что\s+)?/iu, 'Исправить '],
  [/^(?:добавь(?:те)?|сделай(?:те)?|создай(?:те)?)(?=[\s,]|$)\s*/iu, 'Добавить '],
  [/^(?:убери(?:те)?|удали(?:те)?)(?=[\s,]|$)\s*/iu, 'Убрать '],
  [/^(?:вынеси(?:те)?)(?=[\s,]|$)\s*/iu, 'Вынести '],
  [/^(?:перенеси(?:те)?)(?=[\s,]|$)\s*/iu, 'Перенести '],
  [/^(?:перемести(?:те)?)(?=[\s,]|$)\s*/iu, 'Переместить '],
  [/^(?:переработай(?:те)?)(?=[\s,]|$)\s*/iu, 'Переработать '],
  [/^(?:переделай(?:те)?)(?=[\s,]|$)\s*/iu, 'Переработать '],
  [/^(?:оптимизируй(?:те)?|ускорь(?:те)?)(?=[\s,]|$)\s*/iu, 'Оптимизировать '],
  [/^(?:проверь(?:те)?)(?=[\s,]|$)\s*/iu, 'Проверить '],
  [/^(?:исследуй(?:те)?|проанализируй(?:те)?|изучи(?:те)?)(?=[\s,]|$)\s*/iu, 'Исследовать '],
  [/^(?:задокументируй(?:те)?|опиши(?:те)?)(?=[\s,]|$)\s*/iu, 'Задокументировать '],
  [/^(?:настрой(?:те)?)(?=[\s,]|$)\s*/iu, 'Настроить '],
  [/^(?:обнови(?:те)?)(?=[\s,]|$)\s*/iu, 'Обновить '],
  [/^(?:переименуй(?:те)?)(?=[\s,]|$)\s*/iu, 'Переименовать '],
  [/^(?:покажи(?:те)?|отображай(?:те)?)(?=[\s,]|$)\s*/iu, 'Показывать '],
  [/^(?:скрой(?:те)?|спрячь(?:те)?)(?=[\s,]|$)\s*/iu, 'Скрыть '],
  [/^(?:упрости(?:те)?)(?=[\s,]|$)\s*/iu, 'Упростить '],
  [/^(?:объедини(?:те)?)(?=[\s,]|$)\s*/iu, 'Объединить '],
  [/^(?:раздели(?:те)?)(?=[\s,]|$)\s*/iu, 'Разделить '],
  [/^(?:выровняй(?:те)?|выровни(?:те)?)(?=[\s,]|$)\s*/iu, 'Выровнять '],
  [/^(?:почисти(?:те)?|очисти(?:те)?)(?=[\s,]|$)\s*/iu, 'Очистить '],
  [/^(?:запрети(?:те)?)(?=[\s,]|$)\s*/iu, 'Запретить '],
  [/^(?:разреши(?:те)?)(?=[\s,]|$)\s*/iu, 'Разрешить '],
]

/** Инфинитивы действий — «ядро» с таким началом наиболее ценно. */
const ACTION_INFINITIVE =
  /^(?:исправить|добавить|убрать|удалить|вынести|перенести|переместить|переработать|оптимизировать|ускорить|проверить|исследовать|проанализировать|задокументировать|настроить|обновить|переименовать|показывать|показать|скрыть|упростить|объединить|разделить|выровнять|очистить|запретить|разрешить|заменить|привести|реализовать|сделать|подключить|отключить|включить|выключить|стабилизировать|унифицировать|автоматизировать|ограничить|расширить|поддержать|выгружать|экспортировать|импортировать|отправлять|сохранять|открывать|закрывать|подготовить|провести|заменить)(?=[\s,.:;!?]|$)/iu

/**
 * Модули продукта для формата «<Модуль>: <суть>» у дефектов. Словарь —
 * а не свободный вывод: не выдумываем несуществующий контекст.
 */
const MODULES: Array<[RegExp, string]> = [
  [/доск/iu, 'Доска'],
  [/календар/iu, 'Календарь'],
  [/встреч|видеозвон|созвон/iu, 'Встречи'],
  [/голосов|диктовк|микрофон|распознаван/iu, 'Голос'],
  [/уведомлен|колокольчик|напоминан/iu, 'Уведомления'],
  [/спринт/iu, 'Спринты'],
  [/настройк/iu, 'Настройки'],
  [/вложени|аттач/iu, 'Вложения'],
  [/комментар/iu, 'Комментарии'],
  [/авторизац|логин|регистрац/iu, 'Авторизация'],
  [/профил/iu, 'Профиль'],
  [/списк[еа]?\s+задач|список\s+задач/iu, 'Список задач'],
]

function detectModule(text: string): string | null {
  for (const [re, name] of MODULES) if (re.test(text)) return name
  return null
}

// ---------------------------------------------------------------------------
// Выбор «ядра»: предложение с максимальным инженерным сигналом
// ---------------------------------------------------------------------------

function scoreSentence(sentence: string, index: number): number {
  const cleaned = stripFillers(sentence)
  let score = 0
  const actionStart =
    ACTION_INFINITIVE.test(cleaned) || VERB_CANON.some(([re]) => re.test(cleaned))
  if (actionStart) score += 3
  if (DEFECT_SIGNAL.test(cleaned)) score += 3
  if (index === 0) score += 2
  if (index === 1) score += 1
  const words = cleaned.split(/\s+/).length
  // Короткие обрывки («Спасибо.») — штраф, но короткое ДЕЙСТВИЕ
  // («Подготовить отчёт») — полноценное название
  if (words < 3 && !actionStart) score -= 2
  if (words > 25) score -= 1 // простыня — скорее контекст, чем суть
  if (/\?\s*$/.test(sentence)) score -= 2 // вопросы — не формулировка работы
  return score
}

function pickCoreSentence(text: string): string {
  const sentences = splitSentences(text)
  if (sentences.length === 0) return ''
  let best = sentences[0]!
  let bestScore = -Infinity
  sentences.forEach((sentence, index) => {
    const score = scoreSentence(sentence, index)
    if (score > bestScore) {
      bestScore = score
      best = sentence
    }
  })
  return best
}

// ---------------------------------------------------------------------------
// Сборка названия
// ---------------------------------------------------------------------------

/** «При X ломается Y» → «Y ломается при X»: объект проблемы — первым. */
function reorderBreakage(text: string): string {
  const m = text.match(AT_BREAKAGE)
  if (!m) return text
  const verb = text.match(new RegExp(BREAKAGE_VERBS, 'iu'))?.[0] ?? ''
  const [, condition, subject] = m
  return `${subject!.trim()} ${verb} при ${condition!.trim()}`
}

function canonicalizeLeadingVerb(text: string): string {
  for (const [re, replacement] of VERB_CANON) {
    if (re.test(text)) return text.replace(re, replacement).trim()
  }
  return text
}

/** Служебные слова, на которых нельзя обрывать название («… интеграцию с»). */
const DANGLING_TAIL =
  /\s+(?:в|на|по|за|со?|ко?|о|об|от|до|из|у|при|для|и|или|а|но|не|чтобы|как|же|это|его|её|их)$/iu

function clipToWordBoundary(sentence: string): string {
  if (sentence.length <= TITLE_MAX_CHARS) return sentence
  const words = sentence.split(/\s+/)
  let title = words[0] ?? ''
  for (const word of words.slice(1)) {
    if (`${title} ${word}`.length > TITLE_MAX_CHARS) break
    title = `${title} ${word}`
  }
  if (title.length > TITLE_MAX_CHARS) title = title.slice(0, TITLE_MAX_CHARS)
  // Обрезка не должна кончаться предлогом/союзом — снимаем повисшие слова
  let prev = ''
  while (prev !== title) {
    prev = title
    title = title.replace(DANGLING_TAIL, '')
  }
  return title.replace(/[,;:–—-]\s*$/u, '').trim()
}

/** Название задачи из полного текста постановки. Пустой текст → ''. */
export function generateTaskTitle(text: string): string {
  const plain = stripMarkdown(text).trim()
  if (!plain) return ''

  // Ядро выбирается по всему тексту (V2), вводные срезаются до и после
  // выбора: «Необходимо, чтобы …» может прятать суть за вводной.
  const core = pickCoreSentence(plain)
  const cleaned = stripFillers(core)
  const noTail = cutExplanatoryTail(cleaned.replace(/[.!?…]+$/u, '').trim())
  const reordered = reorderBreakage(stripFillers(noTail))
  let title = canonicalizeLeadingVerb(reordered)

  // Дефект-фраза без ведущего глагола действия: «Исправить: <суть>»;
  // если модуль назван в остальном тексте, но не в сути — «<Модуль>: <суть>»
  // (без тавтологии «Доска: … на доске …»).
  const startsWithAction = ACTION_INFINITIVE.test(title)
  if (!startsWithAction && !QUESTION_START.test(title) && DEFECT_SIGNAL.test(title)) {
    const moduleInCore = detectModule(title)
    const prefix = moduleInCore
      ? 'Исправить'
      : (detectModule(plain) ?? 'Исправить')
    // После префикса суть — со строчной (кроме аббревиатур/имён: «API…»)
    const suffix = /^[А-ЯЁA-Z][а-яёa-z]/u.test(title)
      ? title.charAt(0).toLowerCase() + title.slice(1)
      : title
    title = `${prefix}: ${suffix}`
  }

  return capitalizeFirst(clipToWordBoundary(title))
}
