describe('generateTaskTitle V2 — лимит и края', () => {
  it('длинная фраза режется по границе слова и не кончается предлогом', () => {
    const title = generateTaskTitle(
      'Необходимо чтобы при создании задачи автоматически определялось корректное название по описанию, а не использовалась первая строка текста.',
    )
    expect(title).toBe(
      'При создании задачи автоматически определялось корректное название',
    )
    expectQualityGate(title)
  })

  it('обрезка по границе слова до лимита', () => {
    const long = 'Слово '.repeat(30).trim()
    const title = generateTaskTitle(long)
    expect(title.length).toBeLessThanOrEqual(TITLE_MAX_CHARS)
    expect(title.endsWith('Слово')).toBe(true)
  })

  it('сверхдлинное слово не ломает алгоритм', () => {
    const long = 'Добавить ' + 'сверхдлинноесловобезпробелов'.repeat(5)
    expect(generateTaskTitle(long).length).toBeLessThanOrEqual(TITLE_MAX_CHARS)
  })

  it('пустые и пробельные входы → пустая строка', () => {
    expect(generateTaskTitle('')).toBe('')
    expect(generateTaskTitle('   \n\t  ')).toBe('')
  })

  it('текст без пунктуации — одно предложение, ничего не придумываем', () => {
    runCases([
      { input: 'обновить зависимости фронтенда', expected: 'Обновить зависимости фронтенда' },
    ])
  })
})

