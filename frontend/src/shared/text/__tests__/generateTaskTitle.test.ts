import { describe, it, expect } from 'vitest'
import { generateTaskTitle, TITLE_MAX_CHARS } from '../generateTaskTitle'

/**
 * ТП-166 (V2 движка ТП-153): заголовки инженерного уровня из ПОЛНОГО текста
 * постановки. Таблично-параметризованные сценарии по типам работ; каждый
 * результат проходит общий «гейт качества» из ТЗ: ≤70 символов, без точки,
 * без вводных-канцеляризмов, без обрыва на предлоге, с капитализацией.
 */

function expectQualityGate(title: string) {
  expect(title.length).toBeLessThanOrEqual(TITLE_MAX_CHARS)
  expect(title).not.toMatch(/[.]$/u)
  expect(title).not.toMatch(
    /^(необходимо|нужно|надо|прошу|хочу|хотелось|давай|пожалуйста)\b/iu,
  )
  expect(title).not.toMatch(/реализовать\s+возможность/iu)
  expect(title).not.toMatch(
    /\s(?:в|на|по|за|с|к|о|от|до|из|у|при|для|и|или|но|чтобы|как)$/iu,
  )
  expect(title.charAt(0)).toBe(title.charAt(0).toUpperCase())
}

type Case = { input: string; expected: string }

function runCases(cases: Case[]) {
  for (const { input, expected } of cases) {
    const title = generateTaskTitle(input)
    expect(title, `вход: «${input.slice(0, 60)}…»`).toBe(expected)
    expectQualityGate(title)
  }
}

