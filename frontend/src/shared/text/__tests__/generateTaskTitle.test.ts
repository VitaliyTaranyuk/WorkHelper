import { describe, it, expect } from 'vitest'
import { generateTaskTitle, TITLE_MAX_CHARS } from '../generateTaskTitle'

describe('generateTaskTitle (ТП-153, единый движок названий)', () => {
  it('пример из постановки: срез «Необходимо чтобы…» и хвоста «, а не …»', () => {
    expect(
      generateTaskTitle(
        'Необходимо чтобы при создании задачи автоматически определялось корректное название по описанию, а не использовалась первая строка текста.',
      ),
    ).toBe(
      'При создании задачи автоматически определялось корректное название по описанию',
    )
  })

  it('пример из постановки: багрепорт «При X ломается Y» — объект первым', () => {
    expect(
      generateTaskTitle(
        'При изменении размера окна ломается интерфейс списка задач',
      ),
    ).toBe('Интерфейс списка задач ломается при изменении размера окна')
  })

  it('срезает цепочки вводных: «Прошу, сделайте так, чтобы…»', () => {
    expect(
      generateTaskTitle('Прошу, сделайте так, чтобы доска обновлялась сама.'),
    ).toBe('Доска обновлялась сама')
  })

  it('«Нужно …» и «Надо …» не попадают в название', () => {
    expect(generateTaskTitle('Нужно добавить фильтр по исполнителю.')).toBe(
      'Добавить фильтр по исполнителю',
    )
    expect(generateTaskTitle('надо проверить экспорт отчёта')).toBe(
      'Проверить экспорт отчёта',
    )
  })

  it('хвост «, потому что …» уходит в описание, не в название', () => {
    expect(
      generateTaskTitle(
        'Убрать дубль кнопки в шапке, потому что она сбивает пользователей с толку.',
      ),
    ).toBe('Убрать дубль кнопки в шапке')
  })

  it('«не работает» тоже переставляется', () => {
    expect(
      generateTaskTitle('При открытии карточки не работает кнопка сохранения'),
    ).toBe('Кнопка сохранения не работает при открытии карточки')
  })

  it('текст без вводных и паттернов — первая мысль как есть', () => {
    expect(
      generateTaskTitle('Починить выравнивание карточек на доске. Детали внутри.'),
    ).toBe('Починить выравнивание карточек на доске')
  })

  it('обрезка по границе слова до лимита', () => {
    const long = 'Слово '.repeat(30).trim()
    const title = generateTaskTitle(long)
    expect(title.length).toBeLessThanOrEqual(TITLE_MAX_CHARS)
    expect(title.endsWith('Слово')).toBe(true)
  })

  it('пустой ввод → пустая строка; капитализация первой буквы', () => {
    expect(generateTaskTitle('   ')).toBe('')
    expect(generateTaskTitle('фильтр не сохраняется')).toBe(
      'Фильтр не сохраняется',
    )
  })

  it('не режет хвост, если без него остаётся меньше трёх слов', () => {
    expect(generateTaskTitle('Сделать, чтобы работало быстрее.')).toBe(
      'Сделать, чтобы работало быстрее',
    )
  })
})
